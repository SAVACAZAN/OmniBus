pub fn main() !void {
    var metadata: OmniBusWalletMetadata = undefined;

    // Initialize with example data
    const test_mnemonic = "abandon abandon abandon abandon abandon abandon abandon abandon abandon abandon abandon about";
    @memcpy(metadata.mnemonic[0..test_mnemonic.len], test_mnemonic);
    metadata.mnemonic_len = @intCast(test_mnemonic.len);

    metadata.passphrase_len = 0;

    // Example seed (would be generated by PBKDF2 in real impl)
    for (0..64) |i| {
        metadata.seed[i] = @as(u8, @intCast(i % 256));
    }

    // Example master keys
    for (0..32) |i| {
        metadata.master_private_key[i] = @as(u8, @intCast((i + 1) % 256));
        metadata.master_chain_code[i] = @as(u8, @intCast((i + 2) % 256));
    }
    metadata.master_public_key[0] = 0x02; // Compressed prefix
    for (1..33) |i| {
        metadata.master_public_key[i] = @as(u8, @intCast((i + 3) % 256));
    }
    for (0..4) |i| {
        metadata.master_fingerprint[i] = @as(u8, @intCast(i * 73 % 256));
    }

    // Example derived keys
    for (0..32) |i| {
        metadata.private_key[i] = @as(u8, @intCast((i + 10) % 256));
        metadata.chain_code[i] = @as(u8, @intCast((i + 11) % 256));
    }
    metadata.public_key[0] = 0x02;
    for (1..33) |i| {
        metadata.public_key[i] = @as(u8, @intCast((i + 12) % 256));
    }

    // Example addresses
    const pq_addr = "omni_k1_0_a1f2e3d4c5b6a7f8e9d0c1b2a3f4e5d";
    @memcpy(metadata.pq_address[0..pq_addr.len], pq_addr);
    metadata.pq_address_len = @intCast(pq_addr.len);

    const evm_addr = "0x8ba1f109551bD432803012645Ac136ddd64DBA72";
    @memcpy(metadata.evm_address[0..evm_addr.len], evm_addr);
    metadata.evm_address_len = @intCast(evm_addr.len);

    const tap_addr = "bc1pw508d6qejxtdg4y5r3zarvary0c5xw7kw508d6qejxtdg4y5r3zarvary";
    @memcpy(metadata.btc_taproot[0..@min(tap_addr.len, 62)], tap_addr[0..@min(tap_addr.len, 62)]);
    metadata.btc_taproot_len = @intCast(@min(tap_addr.len, 62));

    print_wallet_metadata(&metadata);
}
