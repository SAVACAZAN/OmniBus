// OmniBus Wallet Generation with Complete Metadata
// Shows all keys, addresses, and hierarchy for each token
// Comparable to Bitcoin BIP-32/BIP-44 structure

const std = @import("std");

pub const OmniBusWalletMetadata = struct {
    // ========================================================================
    // BIP-39 MNEMONIC & SEED
    // ========================================================================
    mnemonic: [264]u8,           // 12 or 24 words
    mnemonic_len: u8,
    passphrase: [64]u8,          // Optional BIP-39 passphrase
    passphrase_len: u8,

    // ========================================================================
    // BIP-39 SEED (Root of hierarchy)
    // ========================================================================
    // Generated by: PBKDF2-HMAC-SHA512(mnemonic, "TREZOR" || passphrase)
    seed: [64]u8,                // 512-bit seed (64 bytes)

    // ========================================================================
    // BIP-32 MASTER KEY (Level 0)
    // ========================================================================
    master_private_key: [32]u8,  // Raw master private key
    master_chain_code: [32]u8,   // Chain code for child derivation
    master_public_key: [33]u8,   // Compressed public key (1 + 32 bytes)
    master_fingerprint: [4]u8,   // Fingerprint (first 4 bytes of hash160)

    // Extended keys (for compatibility with standard wallets)
    xpriv: [111]u8,              // Extended private key (serialized)
    xpriv_len: u8,
    xpub: [111]u8,               // Extended public key (serialized)
    xpub_len: u8,

    // ========================================================================
    // BIP-44 PATH METADATA (Level 5: account address)
    // ========================================================================
    derivation_depth: u8,        // Depth in hierarchy (5 for BIP-44)
    coin_type: u32,              // 506=OmniBus, 0=Bitcoin, 60=Ethereum, etc.
    account_index: u8,           // Usually 0
    change_index: u8,            // 0=external, 1=internal
    address_index: u32,          // Address sequence number

    // Fingerprints for hierarchy verification
    parent_fingerprint: [4]u8,   // Fingerprint of parent key
    child_fingerprint: [4]u8,    // Fingerprint of this key

    // ========================================================================
    // DERIVED KEYS & ADDRESSES
    // ========================================================================
    private_key: [32]u8,         // Derived private key (from path)
    public_key: [33]u8,          // Compressed public key
    chain_code: [32]u8,          // Chain code for further derivation

    // Post-Quantum addresses (OmniBus native)
    pq_address: [70]u8,          // omni_k1_/omni_f1_/omni_d1_/omni_s1_
    pq_address_len: u8,

    // EVM Compatible address
    evm_address: [42]u8,         // 0x...
    evm_address_len: u8,

    // Bitcoin Taproot (if applicable)
    btc_taproot: [62]u8,         // bc1p...
    btc_taproot_len: u8,

    // ========================================================================
    // DERIVED DATA
    // ========================================================================
    // Hashes (for address generation)
    pubkey_hash: [20]u8,         // RIPEMD160(SHA256(pubkey))
    pubkey_hash160: [20]u8,      // Full hash160
    keccak256_hash: [32]u8,      // Keccak256(pubkey)

    // WIF (Wallet Import Format) - for classical wallets
    wif: [52]u8,                 // Base58Check encoded private key
    wif_len: u8,

    // Script information
    script_pub_key: [34]u8,      // OP_0 <pubkey_hash> or witness program
    script_pub_key_len: u8,
    witness_program: [32]u8,     // Witness program (Taproot/Segwit)
    witness_version: u8,         // 0=P2WPKH, 1=P2TR, etc.

    // ========================================================================
    // METADATA
    // ========================================================================
    created_at: u64,             // Timestamp
    token_name: [32]u8,          // OMNI, LOVE, FOOD, RENT, VACA
    token_symbol: [8]u8,
    domain_id: u8,               // 0-4
    crypto_algorithm: [32]u8,    // Kyber-768, Falcon-512, etc.
};

